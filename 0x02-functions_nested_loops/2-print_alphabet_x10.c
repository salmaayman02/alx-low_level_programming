/**
 * print_alphabet_x10 - prints "line "
 *
 * Description : program print alphabet
 *
 * Return: no return value(Success)
*/

void print_alphabet_x10(void);
{
	char c = 'a';
	int i=0;

	for(i=0; i<10; i++)
	{
		while (c <= 'z')
		{
			_putchar('c');
			c++;
		}
	_putchar('\n');
	}
}
